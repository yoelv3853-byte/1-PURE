function scrollToGalleryImage(index) {
  const track = document.getElementById('galleryTrack');
  const items = track.querySelectorAll('.gallery-item');
  const item = items[index];
  
  if (item) {
    item.scrollIntoView({ behavior: 'smooth', block: 'nearest', inline: 'start' });
    currentGalleryIndex = index;
  }
}

function updateGalleryDots() {
  const track = document.getElementById('galleryTrack');
  const items = track.querySelectorAll('.gallery-item');
  const scrollPosition = track.scrollLeft;
  const itemWidth = items[0]?.offsetWidth || 550;
  const gap = 40;
  
  let closestIndex = 0;
  let closestDistance = Math.abs(scrollPosition);
  
  items.forEach((item, idx) => {
    const itemPosition = idx * (itemWidth + gap);
    const distance = Math.abs(scrollPosition - itemPosition);
    
    if (distance < closestDistance) {
      closestDistance = distance;
      closestIndex = idx;
    }
  });
  
  currentGalleryIndex = closestIndex;
  
  // Actualizar dots
  document.querySelectorAll('.gallery-dot').forEach((dot, idx) => {
    if (idx === closestIndex) {
      dot.classList.add('active');
    } else {
      dot.classList.remove('active');
    }
  });
}

